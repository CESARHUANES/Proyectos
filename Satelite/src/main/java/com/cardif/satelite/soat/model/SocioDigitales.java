package com.cardif.satelite.soat.model;

public class SocioDigitales {

	private long idTrama;
	private long idPoliza;
	private String estadoPoliza;
	private long ordenCompra;
	private String numValorado;
	
	public long getIdTrama() {
		return idTrama;
	}
	public void setIdTrama(long idTrama) {
		this.idTrama = idTrama;
	}
	public long getIdPoliza() {
		return idPoliza;
	}
	public void setIdPoliza(long idPoliza) {
		this.idPoliza = idPoliza;
	}
	public String getEstadoPoliza() {
		return estadoPoliza;
	}
	public void setEstadoPoliza(String estadoPoliza) {
		this.estadoPoliza = estadoPoliza;
	}
	public long getOrdenCompra() {
		return ordenCompra;
	}
	public void setOrdenCompra(long ordenCompra) {
		this.ordenCompra = ordenCompra;
	}
	public String getNumValorado() {
		return numValorado;
	}
	public void setNumValorado(String numValorado) {
		this.numValorado = numValorado;
	}
	
	
	
	
}
